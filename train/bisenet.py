def dilate_mask(mask, kernel_size=3, iterations=1):
    """
    Dilate a binary mask using PyTorch.

    Parameters:
    mask (torch.Tensor): A binary mask in GPU tensor format.
    kernel_size (int): The size of the square structuring element. Default is 3.
    iterations (int): The number of times the dilation operation should be applied. Default is 1.

    Returns:
    torch.Tensor: Dilated binary mask in GPU tensor format.
    """

    # Convert the mask to float tensor
    mask = mask.float()

    # Create a square structuring element
    struct_elem = torch.ones((1, 1, kernel_size, kernel_size), dtype=torch.float32, device=mask.device)

    # Pad the mask with zeros to handle boundary conditions
    padding = kernel_size // 2
    padded_mask = F.pad(mask, (padding, padding, padding, padding), mode='constant', value=0)

    # Perform the dilation operation
    for _ in range(iterations):
        conv_result = F.conv2d(padded_mask, struct_elem, padding=0)
        threshold_value = 1
        dilated_mask = (conv_result >= threshold_value).float()
        padded_mask = F.pad(dilated_mask, (padding, padding, padding, padding), mode='constant', value=0)

    return dilated_mask

